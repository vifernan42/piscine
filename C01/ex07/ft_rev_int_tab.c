void	ft_rev_int_tab(int *tab, int size)
{
	int tab2[size];
	int j;
	int i;

	i = 0;
	j = 0;
	while (j < size)
	{
		tab2[j] = tab[i];
		i++;
		j++;
	}
	i = 0;
	while (i < size)
	{
		tab[i] = tab2[j - 1];
		i++;
		j--;
	}
}

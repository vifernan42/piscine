char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 97 && str[i] <= 122) && (str[i] == str[0] ||
			(str[i - 1] >= 0 && str[i - 1] <= 47) || (str[i - 1]
			>= 91 && str[i - 1] <= 96) || (str[i - 1]
			>= 123 && str[i - 1] <= 127) || (str[i - 1] >= 58
			&& str[i - 1] <= 64)))
			str[i] = str[i] - 32;
		else if ((str[i] >= 65 && str[i] <= 90) &&
				((str[i - 1] >= 97 && str[i - 1] <= 122) ||
				(str[i - 1] >= 65 && str[i - 1] <= 90) ||
				(str[i - 1] >= 48 && str[i - 1] <= 57)))
			str[i] = str[i] + 32;
		i++;
	}
	return (str);
}
